class Productos {
	constructor(nombre,id,precio,stock){
		this.nombre = nombre;
		this.id = id;
		this.precio = precio;
		this.stock = stock;
	}
}

class Usuario {
	constructor(nombre,correo,telefono,compra){
		this.nombre = nombre;
		this.correo = correo;
		this.telefono = telefono;
		this.compra = compra;
	}
}