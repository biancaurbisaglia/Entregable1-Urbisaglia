class Productos {
	constructor(nombre,codigo,precio,stock){
		this.nombre = nombre;
		this.codigo = codigo;
		this.precio = precio;
		this.stock = stock;
	}
}

class Usuario {
	constructor(nombre,correo,telefono,compra){
		this.nombre = nombre;
		this.correo = correo;
		this.telefono = telefono;
		this.compra = compra;
	}
}